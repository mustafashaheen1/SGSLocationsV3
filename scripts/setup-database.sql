/*
  # SGS Locations Database Setup Script

  Complete database schema and initial data for SGS Locations website.

  ## Tables Created:
  1. users - User accounts (property owners, production pros, admins)
  2. properties - Location listings with full details
  3. bookings - Property reservations and rentals

  ## Security:
  - Row Level Security (RLS) enabled on all tables
  - Policies for public read access and authenticated user management

  ## Data Included:
  - 10 sample users
  - 65+ properties across Dallas/Fort Worth area
  - 20 sample bookings
*/

-- =====================================================
-- STEP 1: DROP EXISTING TABLES (if any)
-- =====================================================

DROP TABLE IF EXISTS bookings CASCADE;
DROP TABLE IF EXISTS properties CASCADE;
DROP TABLE IF EXISTS users CASCADE;

-- =====================================================
-- STEP 2: CREATE TABLES
-- =====================================================

-- USERS TABLE
CREATE TABLE users (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  email TEXT UNIQUE NOT NULL,
  full_name TEXT NOT NULL,
  phone TEXT,
  user_type TEXT NOT NULL CHECK (user_type IN ('production', 'property_owner', 'admin')),
  company_name TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- PROPERTIES TABLE
CREATE TABLE properties (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name TEXT NOT NULL,
  description TEXT NOT NULL,
  address TEXT NOT NULL,
  city TEXT NOT NULL,
  county TEXT NOT NULL,
  zipcode TEXT NOT NULL,
  property_type TEXT NOT NULL CHECK (property_type IN ('Residential', 'Commercial', 'Natural', 'Industrial', 'Historical')),
  square_footage INTEGER,
  lot_size DECIMAL(10,2),
  bedrooms INTEGER,
  bathrooms DECIMAL(3,1),
  parking_spaces INTEGER,
  year_built INTEGER,
  features TEXT[] DEFAULT '{}',
  categories TEXT[] DEFAULT '{}',
  permits_available BOOLEAN DEFAULT false,
  permit_details TEXT,
  daily_rate DECIMAL(10,2) NOT NULL,
  images TEXT[] DEFAULT '{}',
  primary_image TEXT,
  status TEXT DEFAULT 'active' CHECK (status IN ('active', 'pending', 'inactive')),
  is_featured BOOLEAN DEFAULT false,
  is_exclusive BOOLEAN DEFAULT false,
  view_count INTEGER DEFAULT 0,
  owner_id UUID REFERENCES users(id),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- BOOKINGS TABLE
CREATE TABLE bookings (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  property_id UUID REFERENCES properties(id) ON DELETE CASCADE,
  user_id UUID REFERENCES users(id) ON DELETE CASCADE,
  start_date DATE NOT NULL,
  end_date DATE NOT NULL,
  status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'confirmed', 'cancelled', 'completed')),
  total_cost DECIMAL(10,2) NOT NULL,
  notes TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- =====================================================
-- STEP 3: CREATE INDEXES FOR PERFORMANCE
-- =====================================================

CREATE INDEX idx_properties_city ON properties(city);
CREATE INDEX idx_properties_property_type ON properties(property_type);
CREATE INDEX idx_properties_status ON properties(status);
CREATE INDEX idx_properties_is_featured ON properties(is_featured);
CREATE INDEX idx_properties_categories ON properties USING GIN(categories);
CREATE INDEX idx_properties_features ON properties USING GIN(features);
CREATE INDEX idx_bookings_property_id ON bookings(property_id);
CREATE INDEX idx_bookings_user_id ON bookings(user_id);
CREATE INDEX idx_bookings_status ON bookings(status);

